def normalize_lang(lang: str | None) -> str:
    return lang if lang in SUPPORTED_LANGS else DEFAULT_LANG


def t(lang: str | None, key: str, **values: Any) -> str:
    language = normalize_lang(lang)
    text = TRANSLATIONS.get(language, TRANSLATIONS[DEFAULT_LANG]).get(
        key, TRANSLATIONS[DEFAULT_LANG].get(key, key)
    )
    return text.format(**values) if values else text


def theme_choices(lang: str | None) -> list[tuple[str, str]]:
    return [
        (t(lang, "theme_dark"), "dark"),
        (t(lang, "theme_light"), "light"),
    ]
